    private void BotaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExcluirActionPerformed
        int opcao = jListaConsulta.getSelectedRow();
        
        if(opcao>=0){
        // Obter os dados do paciente selecionado na tabela
        ConsultaDTO c= new ConsultaDTO();
        c.setIdConsulta((int) jListaConsulta.getValueAt(opcao, 0));
        c.setIdPaciente((int) jListaConsulta.getValueAt(opcao, 1));
        c.setIdMedico((int) jListaConsulta.getValueAt(opcao, 2));

        // Convers√£o da data do formato yyyy/MM/dd para dd/MM/yyyy
        String dataN = jListaConsulta.getValueAt(opcao, 3).toString();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        DateTimeFormatter formatterSis = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNas = LocalDate.parse(dataN, formatter);
        //String dataNascForm = dataNas.format(formatterSis);
        c.setDataConsulta(dataNas);
        
        String horaConsultaStr = jListaConsulta.getValueAt(opcao, 4).toString();
        LocalTime horaConsulta = LocalTime.parse(horaConsultaStr);
        c.setHoraConsulta(horaConsulta);
    
        ConsultaDAO cDAO = new ConsultaDAO();
        cDAO.excluirConsulta(c);
        preencherTabela();
        new ListarConsultas().setVisible(true);
        dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
            new ListarPacientes().setVisible(true);
            dispose();
       }  
    }//GEN-LAST:event_BotaoExcluirActionPerformed
    public void preencherTabela(){
        ConsultaDAO cDAO = new ConsultaDAO();
        List<ConsultaDTO> lista = cDAO.listarConsultas();
        DefaultTableModel tabela = (DefaultTableModel)jListaConsulta.getModel();
        
        tabela.setRowCount(0);
        for(ConsultaDTO cDTO : lista){
            tabela.addRow(new Object[]{cDTO.getIdConsulta(),cDTO.getIdPaciente(),cDTO.getIdMedico(), cDTO.getDataConsulta(), cDTO.getHoraConsulta()});
        }
    }
    
